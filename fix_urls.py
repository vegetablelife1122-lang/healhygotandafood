fixed_count = 0
not_found = []

for name, (old_id, new_id) in corrections.items():
    old_url = base_url + old_id + "/"
    new_url = base_url + new_id + "/"
    escaped_name = re.escape(name)
    pattern = r'(name:\s*["\']' + escaped_name + r'["\'].*?tabelogUrl:\s*")' + re.escape(old_url) + r'"'
    replacement = r'\g<1>' + new_url + '"'
    new_content = re.sub(pattern, replacement, content, flags=re.DOTALL)
    if new_content == content:
        not_found.append(f"{name}: {old_id} -> {new_id}")
